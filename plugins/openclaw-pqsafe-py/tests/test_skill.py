class TestVerifyReceived:
    """Test verify_received action against TC1-TC4 test vector envelopes."""

    @pytest.fixture
    def skill(self):
        from pqsafe_openclaw.skill import PQSafeSkill

        return PQSafeSkill(mock_mode=True)

    @pytest.mark.parametrize("tc_id", ["tc1", "tc2", "tc3", "tc4"])
    def test_verify_received_tc_envelopes(self, skill, vectors, tc_id, mldsa_public_key_bytes):
        """
        verify_received() must accept the TypeScript-generated dual-signed envelopes.

        This is the primary cross-language interoperability proof for the skill layer:
        a Python agent receiving an envelope signed by the TypeScript SDK must be
        able to verify it without re-signing.
        """
        try:
            from pqcrypto.sign.ml_dsa_65 import verify  # type: ignore[import]  # noqa: F401
        except ImportError:
            pytest.skip("pqcrypto not installed — verify_received tests skipped")

        tc = vectors[tc_id]
        envelope = tc["dual_signed_envelope"]

        result = skill.verify_received(
            envelope=envelope,
            mldsa_public_key_hex=mldsa_public_key_bytes.hex(),
        )

        assert result.ok, (
            f"{tc_id}: verify_received() failed: {result.error}"
        )
        assert result.result["valid"] is True
        assert result.result["mldsa_verified"] is True

    @pytest.mark.parametrize("tc_id", ["tc1", "tc3"])
    def test_verify_received_with_expected_recipient(self, skill, vectors, tc_id, mldsa_public_key_bytes):
        """verify_received with expected_recipient check."""
        try:
            from pqcrypto.sign.ml_dsa_65 import verify  # type: ignore[import]  # noqa: F401
        except ImportError:
            pytest.skip("pqcrypto not installed")

        tc = vectors[tc_id]
        envelope = tc["dual_signed_envelope"]
        mandate = tc["mandate_input"]
        expected_recipient = mandate.get("recipient")

        if not expected_recipient:
            pytest.skip(f"{tc_id} has no single recipient field")

        result = skill.verify_received(
            envelope=envelope,
            mldsa_public_key_hex=mldsa_public_key_bytes.hex(),
            expected_recipient=expected_recipient,
        )
        assert result.ok, (
            f"{tc_id}: verify_received with expected_recipient={expected_recipient!r} failed: {result.error}"
        )

    def test_verify_received_wrong_pubkey_fails(self, skill, vectors):
        """verify_received with wrong public key must fail."""
        try:
            from pqcrypto.sign.ml_dsa_65 import generate_keypair  # type: ignore[import]
        except ImportError:
            pytest.skip("pqcrypto not installed")

        envelope = vectors["tc1"]["dual_signed_envelope"]
        # Use a fresh (wrong) public key
        pk, _ = generate_keypair()
        wrong_pk_hex = bytes(pk).hex()

        result = skill.verify_received(
            envelope=envelope,
            mldsa_public_key_hex=wrong_pk_hex,
        )
        assert not result.ok, "Expected failure with wrong public key but got success"
        assert "failed" in result.error.lower() or "invalid" in result.error.lower()

    def test_verify_received_wrong_recipient_fails(self, skill, vectors, mldsa_public_key_bytes):
        """verify_received with wrong expected_recipient must fail."""
        try:
            from pqcrypto.sign.ml_dsa_65 import verify  # type: ignore[import]  # noqa: F401
        except ImportError:
            pytest.skip("pqcrypto not installed")

        envelope = vectors["tc1"]["dual_signed_envelope"]

        result = skill.verify_received(
            envelope=envelope,
            mldsa_public_key_hex=mldsa_public_key_bytes.hex(),
            expected_recipient="did:web:wrong-vendor.com:payee:nobody",
        )
        assert not result.ok
        assert "not found" in result.error.lower()
