class TestMLDSAArgOrder:
    """
    Guard against the argument-order trap in ML-DSA signing functions.

    pqcrypto.sign.ml_dsa_65.sign(secret_key, message) — SECRET KEY FIRST.
    @noble/post-quantum v0.6.0 sign(message, secretKey) — MESSAGE FIRST.

    Any regression (swapping args) produces a signature that does NOT verify,
    causing these tests to fail immediately rather than silently.
    """

    def test_mldsa_sign_then_verify(self):
        """Sign a test message and verify it — catches arg-order regressions."""
        try:
            from pqcrypto.sign.ml_dsa_65 import (  # type: ignore[import]
                generate_keypair,
                sign,
                verify,
            )
        except ImportError:
            pytest.skip("pqcrypto not installed — ML-DSA-65 tests skipped")

        message = b"pqsafe openclaw arg-order test 2026-05-01"

        pk, sk = generate_keypair()
        pk, sk = bytes(pk), bytes(sk)

        # CORRECT order: sign(secret_key, message)
        sig = bytes(sign(sk, message))

        # Verify: verify(public_key, message, signature)
        try:
            result = verify(pk, message, sig)
            ok = result if isinstance(result, bool) else True
        except Exception:
            ok = False

        assert ok, (
            "ML-DSA-65 sign+verify failed. "
            "Check argument order: pqcrypto uses sign(sk, msg), verify(pk, msg, sig)."
        )

    def test_mldsa_wrong_order_fails(self):
        """
        Swapping secret key and message in sign() should produce a signature
        that fails verification. This test documents the arg-order trap explicitly.
        """
        try:
            from pqcrypto.sign.ml_dsa_65 import (  # type: ignore[import]
                generate_keypair,
                sign,
                verify,
            )
        except ImportError:
            pytest.skip("pqcrypto not installed")

        # Generate a short message (< secret key size) so swapping is obvious
        message = b"short message"
        pk, sk = generate_keypair()
        pk, sk = bytes(pk), bytes(sk)

        # Sign correctly
        correct_sig = bytes(sign(sk, message))

        # Signing with args swapped: sign(message, sk) would use 'message' as the key.
        # This would either raise an exception (key wrong size) or produce garbage.
        # Either outcome is acceptable — we just confirm it differs from correct_sig.
        try:
            wrong_sig = bytes(sign(message, sk))
            # If it didn't raise, the signatures must be different
            assert wrong_sig != correct_sig, (
                "CRITICAL: sign(message, sk) == sign(sk, message) — "
                "pqcrypto is treating args symmetrically which is unexpected. "
                "Review the API documentation."
            )
        except Exception:
            # Expected: pqcrypto should reject wrong-sized key bytes
            pass  # This is the correct outcome — wrong arg order raises

    def test_hashmldsa_round_trip(self):
        """HashML-DSA mode (SHA-256 pre-hash) round-trips correctly."""
        try:
            from pqcrypto.sign.ml_dsa_65 import (  # type: ignore[import]
                generate_keypair,
                sign,
                verify,
            )
        except ImportError:
            pytest.skip("pqcrypto not installed")

        message = b"HashML-DSA test: pre-hash SHA-256 then sign digest"
        pk, sk = generate_keypair()
        pk, sk = bytes(pk), bytes(sk)

        # HashML-DSA: pre-hash
        digest = hashlib.sha256(message).digest()
        sig = bytes(sign(sk, digest))

        # Verify using digest
        try:
            result = verify(pk, digest, sig)
            ok = result if isinstance(result, bool) else True
        except Exception:
            ok = False

        assert ok, "HashML-DSA round-trip failed — check sign/verify arg order"


