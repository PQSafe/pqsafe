class TestJCSCanonical:
    """Verify Python JCS output matches TypeScript reference implementation byte-for-byte."""

    def _get_jcs_module(self):
        """Import JCS function from pqsafe_openclaw or pqsafe SDK."""
        try:
            import canonicaljson as cj
            return cj.encode_canonical_json
        except ImportError:
            pytest.skip("canonicaljson not installed")

    @pytest.mark.parametrize("tc_id", ["tc1", "tc2", "tc3", "tc4"])
    def test_jcs_bytes_match_reference(self, vectors, tc_id):
        """
        JCS canonicalization of the mandate body must produce byte-for-byte
        identical output to the TypeScript reference implementation.

        This is the foundation of cross-language interoperability: both
        Python and TypeScript must produce identical JCS bytes before signing,
        otherwise signatures will never verify cross-language.
        """
        tc = vectors[tc_id]
        mandate_input = tc["mandate_input"]

        # Remove signature field if present (body-only for JCS)
        body = {k: v for k, v in mandate_input.items() if k != "signature"}

        encode_fn = self._get_jcs_module()
        python_jcs: bytes = encode_fn(body)

        # Compare to reference hex from TypeScript
        reference_hex = tc["jcs_hex"]
        reference_bytes = bytes.fromhex(reference_hex)

        assert python_jcs == reference_bytes, (
            f"{tc_id}: JCS bytes mismatch\n"
            f"  Python:    {python_jcs.hex()}\n"
            f"  Reference: {reference_hex}\n"
            f"  Python decoded:    {python_jcs.decode('utf-8')}\n"
            f"  Reference decoded: {reference_bytes.decode('utf-8')}"
        )

    @pytest.mark.parametrize("tc_id", ["tc1", "tc2", "tc3"])
    def test_jcs_base64url_match_reference(self, vectors, tc_id):
        """JCS bytes must also match the reference Base64url encoding."""
        tc = vectors[tc_id]
        mandate_input = tc["mandate_input"]
        body = {k: v for k, v in mandate_input.items() if k != "signature"}

        encode_fn = self._get_jcs_module()
        python_jcs: bytes = encode_fn(body)
        python_b64 = _b64url_encode(python_jcs)

        reference_b64 = tc["jcs_base64url"]
        assert python_b64 == reference_b64, (
            f"{tc_id}: JCS Base64url mismatch\n"
            f"  Python:    {python_b64}\n"
            f"  Reference: {reference_b64}"
        )

    def test_jcs_key_ordering(self):
        """JCS sorts keys alphabetically (UTF-16 / BMP = Unicode codepoint order)."""
        try:
            import canonicaljson as cj
        except ImportError:
            pytest.skip("canonicaljson not installed")

        obj = {"z": 1, "a": 2, "m": 3}
        result = cj.encode_canonical_json(obj)
        assert result == b'{"a":2,"m":3,"z":1}'

    def test_jcs_array_order_preserved(self, vectors):
        """TC2: array element order is preserved (NOT sorted)."""
        tc = vectors["tc2"]
        body = {k: v for k, v in tc["mandate_input"].items() if k != "signature"}

        try:
            import canonicaljson as cj
        except ImportError:
            pytest.skip("canonicaljson not installed")

        jcs_bytes = cj.encode_canonical_json(body)
        jcs_str = jcs_bytes.decode("utf-8")

        # The three payees appear in the order: aw_zzzlast, acct_aaafirst, P999middle
        # This is the INPUT order, not alphabetical. JCS must preserve array ordering.
        aw_pos = jcs_str.index("aw_zzzlast")
        aa_pos = jcs_str.index("acct_aaafirst")
        p9_pos = jcs_str.index("P999middle")
        assert aw_pos < aa_pos < p9_pos, (
            "TC2: array elements must be in input order (aw < acct < P999), "
            f"got positions: aw={aw_pos}, acct={aa_pos}, P999={p9_pos}"
        )

    def test_jcs_decimal_normalization(self, vectors):
        """TC3: 1.10 must serialize as 1.1 (no trailing zeros)."""
        tc = vectors["tc3"]
        body = {k: v for k, v in tc["mandate_input"].items() if k != "signature"}

        try:
            import canonicaljson as cj
        except ImportError:
            pytest.skip("canonicaljson not installed")

        jcs_str = cj.encode_canonical_json(body).decode("utf-8")
        assert '"spend_cap":1.1' in jcs_str, (
            f"TC3: expected '\"spend_cap\":1.1' in JCS output, got: {jcs_str}"
        )
        assert '"spend_cap":1.10' not in jcs_str, "TC3: trailing zeros must not appear"


