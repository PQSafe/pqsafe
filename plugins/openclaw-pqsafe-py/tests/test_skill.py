class TestCrossLanguageVerification:
    """
    Verify TypeScript-generated ML-DSA-65 signatures using the Python pqcrypto library.

    These tests are the definitive proof of cross-language interoperability:
    signatures produced by the TypeScript SDK (@noble/post-quantum v0.6.0)
    must verify successfully using Python pqcrypto.

    Mode note: the TypeScript test vectors were confirmed (via empirical test) to
    use HashML-DSA mode: SHA-256(JCS bytes) is signed, NOT the raw JCS bytes.
    This is consistent with the AP2 RFC v8 §"Proposed Solution" (HashML-DSA §5.4).
    Pure-mode verification (signing raw JCS) fails on these vectors.
    The OpenClaw Python skill matches this: _mldsa_sign_hashmldsa() pre-hashes.
    """

    @pytest.mark.parametrize("tc_id", ["tc1", "tc2", "tc3", "tc4"])
    def test_mldsa_sig_verifies_with_python(self, vectors, tc_id, mldsa_public_key_bytes):
        """
        TypeScript-generated ML-DSA-65 signatures verify in Python (HashML-DSA mode).

        The TypeScript reference implementation signs SHA-256(JCS bytes) — i.e.
        HashML-DSA mode. Verification must also use SHA-256(JCS bytes), not the
        raw JCS bytes. This was confirmed empirically: pure-mode verification
        returns False; HashML-DSA verification returns True.

        pqcrypto arg order: verify(public_key, message, signature) — PK FIRST.
        """
        try:
            from pqcrypto.sign.ml_dsa_65 import verify  # type: ignore[import]
        except ImportError:
            pytest.skip("pqcrypto not installed — cross-language tests skipped")

        tc = vectors[tc_id]
        jcs_bytes = bytes.fromhex(tc["jcs_hex"])
        mldsa_sig = _b64url_decode(tc["mldsa_sig_base64url"])

        # HashML-DSA: verify against SHA-256 digest of JCS bytes, not raw bytes
        digest = hashlib.sha256(jcs_bytes).digest()

        try:
            result = verify(mldsa_public_key_bytes, digest, mldsa_sig)
            ok = result if isinstance(result, bool) else True
        except Exception as exc:
            ok = False
            pytest.fail(
                f"{tc_id}: ML-DSA-65 verify raised {type(exc).__name__}: {exc}\n"
                f"pqcrypto arg order: verify(pk, message, sig) — PK FIRST."
            )

        assert ok, (
            f"{tc_id}: TypeScript-generated ML-DSA-65 signature did NOT verify in Python "
            "(HashML-DSA mode: verifying SHA-256 digest). "
            "Cross-language interoperability broken. "
            "Possible causes: (1) wrong arg order in verify(), "
            "(2) pqcrypto version mismatch, (3) JCS bytes differ."
        )

    @pytest.mark.parametrize("tc_id", ["tc1", "tc2", "tc3", "tc4"])
    def test_dual_signed_envelope_structure(self, vectors, tc_id):
        """
        Dual-signed envelope must have the correct structure.
        """
        tc = vectors[tc_id]
        envelope = tc["dual_signed_envelope"]

        assert "signature" in envelope, f"{tc_id}: missing 'signature' block"
        sig = envelope["signature"]
        assert sig["alg"] == "ap2-ecdsa-p256+ap2-mldsa65", f"{tc_id}: wrong alg"
        assert "ecdsa" in sig, f"{tc_id}: missing ecdsa signature"
        assert "mldsa" in sig, f"{tc_id}: missing mldsa signature"
        assert "pubkey_fingerprint" in sig, f"{tc_id}: missing pubkey_fingerprint"

        # Fingerprint must match the known public key fingerprint
        expected_fingerprint = vectors["mldsa_pubkey_fingerprint_16"]
        assert sig["pubkey_fingerprint"] == expected_fingerprint, (
            f"{tc_id}: pubkey_fingerprint mismatch: "
            f"got {sig['pubkey_fingerprint']}, expected {expected_fingerprint}"
        )

    def test_tc1_fingerprint_is_sha256_prefix(self, vectors):
        """
        Pubkey fingerprint = first 8 bytes (16 hex chars) of SHA-256(public_key).
        This is the deterministic cross-language fingerprint format.
        """
        pk_b64 = vectors["mldsa_public_key_base64url"]
        pk_bytes = _b64url_decode(pk_b64)

        computed = hashlib.sha256(pk_bytes).hexdigest()[:16]
        expected = vectors["mldsa_pubkey_fingerprint_16"]

        assert computed == expected, (
            f"Fingerprint mismatch: computed={computed}, expected={expected}"
        )


